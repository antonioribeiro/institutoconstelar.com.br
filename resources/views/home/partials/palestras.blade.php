<p>
    Eventos organizados em formatos de palestras, oficinas e workshops.
    As palestras são apresentações sobre temas específicos. Por ter temas específicos, nem sempre serão abertas ao público em geral.
    As oficinas trazem uma proposta terapêutica e serão voltadas aos clientes. Sempre serão para experimentação vivencial de algum tema de interesse geral.
    Os workshops são propostas voltadas tanto para clientes, como para psicólogos, de acordo com o tema que será abordado. Tem uma duração maior que as oficinas, e pode ser dividido parte do tempo para conteúdo teórico, e outra parte para experimentação vivencial.
    Todos os eventos tem como objetivo proporcionar uma experiência de troca e aprendizagem.
</p>
