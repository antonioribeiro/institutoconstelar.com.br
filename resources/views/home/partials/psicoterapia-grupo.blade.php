<p>
    A psicoterapia de grupo trabalha a partir de uma perspectiva da potência psicoterapeutica do grupo. Os grupos são acompanhados por até dois psicólogos que coordenam o trabalho, que pode ser orientado ou definido pelo grupo, de acordo com a proposta de cada trabalho.
    O grupo, por sua diversidade, proporciona uma troca terapêutica interessante, diferente do que acontece no trabalho individual.
</p>
