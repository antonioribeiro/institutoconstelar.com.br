<p class="section-text">
    A Gestalt terapia é uma das abordagens da psicologia e trabalha com uma perspectiva integradora do homem, buscando vê-lo em sua totalidade. Ou seja, o ser humano como pessoa social, biológica, emocional, espiritual, integrando todas as suas formas de ser e estar no mundo.
</p>

<p class="section-text">
    Cada pessoa é única e a Gestalt terapia entende que a forma de ser de cada um é sempre a melhor forma possível, de acordo com o momento e as possibilidades existentes. A resposta que temos para cada situação, ainda que nosso pensamento nos diga que poderíamos ter feito diferente, é sempre a melhor possível para o momento. Entendendo que fazemos parte de um espaço, num determinado tempo, onde nos relacionamos com determinadas pessoas, e todo esse contexto irá impactar em nossas reações e respostas às situações.
</p>

